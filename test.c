int main()
{
	char ch[10000][11], a[11];
	FILE* fp, * fp1;
	int i, j, n = 0;
	if ((fp = fopen("case1.in", "r")) == NULL)
		return 0;
	if ((fp1 = fopen("answer.txt", "w")) == NULL)
		return 0;
	while ((fscanf(fp, "%s", ch[n])) > 0) n++;
	for (i = 0; i < n - 1; i++)
	{
		for (j = 0; j < n - i - 1; j++)
		{
			if (strcmp(ch[j], ch[j + 1]) > 0)
			{
				strcpy(a, ch[j]);
				strcpy(ch[j], ch[j + 1]);
				strcpy(ch[j + 1], a);
			}
		}
	}
	for (i = 0; i < n; i++)
		fprintf(fp1, "%s\n", ch[i]);
	fclose(fp);
	fclose(fp1);
	return 0;
}